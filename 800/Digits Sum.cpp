#include <iostream>
#include <math.h>

using namespace std;

int main()
{ 
    // Number of test Cases
    int test_cases;
    cin >> test_cases;

    // Looping for Each Test Case
    for (int test_case = 0; test_case < test_cases; test_case++) {
        int case_val;
        cin >> case_val;

        // for every
        cout << (int) ++case_val/10 << endl;
        
    }
 
    return 0;
}